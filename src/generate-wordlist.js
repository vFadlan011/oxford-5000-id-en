function filter(word) {
  let strippedWords = [
    "long-term",
    "old-fashioned",
    "T-shirt",
    "decision-making",
    "film-maker",
    "full-time",
    "high-profile",
    "large-scale",
    "line-up",
    "long-standing",
    "long-time",
    "part-time",
    "make-up",
    "post-war",
    "non-profit",
    "set-up",
    "short-term",
    "so-called",
    "thought-provoking",
    "well-being",
  ];

  if (strippedWords.includes(word)) {
    return true;
  }

  if (
    word.includes("-") ||
    word.includes(" ") ||
    word.includes("1") ||
    word.includes("2")
  ) {
    return false;
  } else {
    return true;
  }
}

oxford5k.forEach((word) => {
  if (!oxford_combined.includes(word) && filter(word)) {
    oxford_combined.push(word.toLowerCase());
  }
});

oxford3k.forEach((word) => {
  if (!oxford_combined.includes(word) && filter(word)) {
    // console.log("add o3k: ", word);
    oxford_combined.push(word.toLowerCase());
  }
});

// sort case-insensitive
oxford_combined = oxford_combined.sort(function (a, b) {
  return a.toLowerCase().localeCompare(b.toLowerCase());
});

